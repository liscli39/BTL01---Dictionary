    private boolean compare(String search,String var){
        if(search.length() > var.length()) return false; 
        for(int i=0;i<Math.min(search.length(), var.length());i++){
            if( (search.charAt(i)!=var.charAt(i)) ) return false;
        }
        return true;
    }
